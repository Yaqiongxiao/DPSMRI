    {'cerebellum', sprintf('%s.nii', Key)}, {'cerebellum', sprintf('Affine_%s_seg1.mat', Key)},...
    {'cerebellum', sprintf('u_a_%s_seg1.nii', Key)}, {'cerebellum', sprintf('c_%s_pcereb.nii', Key)},...
